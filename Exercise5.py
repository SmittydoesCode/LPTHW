my_name = 'Will Smith' #Fresh Prince
my_age = 23 #years
my_height = 76 #inches
my_weight = 190 #pounds
my_eyes = 'Green'
my_teeth = 'White'
my_hair = 'Brown'

print(f"Let's talk about {my_name}.") #{} embedds a variable and f after print formats it.
print(f"He is {my_height} inches tall.")
print(f"He is {my_weight} pounds.")
print(f"He has {my_eyes} eyes and {my_hair} hair.")
print(f"His teeth are usually {my_teeth} depending on the coffee.")
total = my_age + my_height + my_weight
print(f"If I add {my_age} + {my_height} + {my_weight}, I get {total}.")

my_height_millimeters = my_height * 25.4
print(f"If I were to convert {my_height} from inches to millimeters, I'd be {my_height_millimeters}mm tall.")
